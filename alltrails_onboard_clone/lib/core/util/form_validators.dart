import 'package:alltrails_onboard_clone/core/enum/string_constant_enum.dart';

String? validateEmail(String? mail) {
  if (mail == null || mail.isEmpty) return StringConstantEnum.errorEmailFieldEmpty.name;
  const pattern = r"(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'"
      r'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-'
      r'\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*'
      r'[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:(2(5[0-5]|[0-4]'
      r'[0-9])|1[0-9][0-9]|[1-9]?[0-9]))\.){3}(?:(2(5[0-5]|[0-4][0-9])|1[0-9]'
      r'[0-9]|[1-9]?[0-9])|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\'
      r'x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])';
  final emailRegex = RegExp(pattern);

  if (!emailRegex.hasMatch(mail)) return StringConstantEnum.errorEmailFieldInvalid.name;

  return null;
}

String? validatePassword(String? password) {
  if (password == null || password.isEmpty) return StringConstantEnum.errorPasswordFieldEmpty.name;
  return null;
}
